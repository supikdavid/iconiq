<div class="left">
<p class="bold">Po několika chladných měsících opět nastává období, které přeje novým zážitkům, novému poznávání, novým dobrodružstvím. To jde ruku v ruce s balením svých osobních věcí na několik dní dopředu. Aktivita, která některé baví, pro jiné znamená doslova noční můru.</p>

<p>Zabalit si dobře na cesty není vůbec jednoduché a pro některé je tento proces malým hlavolamem. Jak místo v kufru opticky zvětšit, co nesmíte zapomenout, a jak předejít tomu, že vezmete spoustu věcí naprosto zbytečně?</p>

<h2>Mrkněte na předpověď</h2>

<p>Ať už plánujete cestovat po naší zemi či se chystáte na opačnou stranu polokoule, vždy si před odjezdem zkontrolujte pomocí internetových či mobilních aplikací aktuální předpověď počasí a její průběh na několik dní dopředu. Při balení kufru ale neberte v potaz jen tuto informaci, dále se zamyslete nad tím, na jak dlouho a jakým způsobem budete svůj pobyt trávit. Je opravdu dost velký rozdíl, pokud si jedete odpočinout na pár dní k moři, jedete si aktivně zasportovat nebo raději upřednostníte horskou cyklistiku.</p>

<p>Základem jsou kousky neutrálních barev, které lze snadno kombinovat. Nebojte se promyslet si outfit na každý den, aby vás nepřekvapilo, že jste si s sebou vzali sice krásné věci, jež k sobě vzájemně ovšem vůbec neladí. Dejte přednost pohodlným materiálům, které se namačkají, flitry a výšivky nechte pro jednou doma. Nenápadné modely lze lehce doladit pomocí originálních a výraznějších doplňků.</p>

<h2>Od nejtěžšího</h2>

<p>V momentě, kdy začnete balit, si vedle sebe vyskládejte všechny předměty, které si užijí dovolenou po vašem po boku a seskládejte si je od nejtěžších věcí, které půjdou na dno kufru jako první, utvoří tak pevný základ. Dobrým podkladem může být například kosmetika či knihy. Následuje oblečení z pevnějšího materiálu, utvořené skulinky prokládáme věcmi křehčími nebo těmi, které mají atypický tvar. Na samotný povrch skládáme například nemačkavá trička, šátky nebo spodní prádlo.</p>

<h2>Vyplatí se rolovat?</h2>

<p>Návodů na to, jak efektivně zabalit kufr, existují na internetu stovky. V posledních letech je ovšem čím dál populárnější balení jednotlivých kousků oděvu do ruliček – zabráníte tak jejich nadměrnému mačkání a ušetříte spoustu místa. Inspiraci pro tento typ balení hledejte např. ve videu značky Louis Vuitton.</p>

<p>Někteří dávají přednost kombinaci různých postupů, skládají jednotlivé oděvy přes sebe a pro trička a další kousky z lehkých materiálů využívají vakuové sáčky, které z prostoru vytáhnou přebytečný objem vzduchu.  Je dobré využít místa uvnitř obuvi (kterou si raději zabalte, aby nedošlo k ušpinění ostatních věcí), vyskládat ji smotanými ponožkami či zabalenou kosmetikou. Aby nedošlo k újmě na kráse některého kousku z vašeho kufru, vyskládejte jeho strany pomocí triček nebo ručníků. Ty nejdůležitější věci, nebo předměty se sklonem k rozbití, se snažte umístit do středu kufru, košile a šaty mají přijít až na úplný konec</p>

<h2>Hlavně nepanikařte</h2>

<p>Často se některým z nás stane, že ustavičným přemýšlením nad všemi okolnostmi dovolené, zapomeneme doma základní praktické věci, jakými jsou kartáčky na zuby, plavky, roztok na kontaktní čočky nebo oblíbenou voděodolnou řasenku. Věřte, že kartáček na zuby si vždy někde koupíte, cestovní pas ovšem jen těžko. Tudíž je důležité směřovat pozornost primárně na důležité věci, bez kterých se během svých cest rozhodně neobejdete.</p>

<p>Balení kufru nemá být noční můra, ale zábava, díky které člověk poznává tak trochu i sám sebe. Najednou vidí, co vše je pro něj důležité a bez čeho se lze v dnešním materiálním světě poměrně snadno obejít. Takže hurá na balení, zkušenosti přece dělají mistry!</p>

<p>P. S. Hezkou dovolenou!</p>

